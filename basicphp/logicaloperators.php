<?php 
// and, bernilai true jika keduanya true
var_dump(true && true);
var_dump(true and false);
// or, bernilai true walaupun salah satunya false
var_dump(true || false);
var_dump(false or false);
// not, kebalikannya
var_dump(!false);
// xor, bernilai true jika salah satunya false
var_dump(true xor true);
?>