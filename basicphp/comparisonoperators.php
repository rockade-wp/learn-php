<?php 
// ini adalah operator perbandingan
$a = 6;
$b = "6";
// sama dengan (==)
var_dump($a == $b);
// identik (===)
var_dump($a === $b);
// tidak sama dengan (!=)
var_dump($a != $b);
// tidak sama dengan (<>) : cara 2
var_dump($a <> $b);
// tidak identik (!==)
var_dump($a !== $b);
// kurang dari(<)
var_dump($a < $b);
// kurang dari sama dengan (<=)
var_dump($a <= $b);
// lebih dari (>)
var_dump($a > $b);
// lebih dari sama dengan (>=)
var_dump($a >= $b);
?>